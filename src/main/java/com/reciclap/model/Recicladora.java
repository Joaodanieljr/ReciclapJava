package com.reciclap.model;

public class Recicladora {

	private long id;
	private String name;
	private String email;
	private String senha;
	private String cnpj;
	private String cep;
	private String endereco;
	private String numero;
	private String cidade;
	private String estado;
	private String telefone;
	private String horario;
	private String diaTrabalho;
	private String materialReciclado;
	
	
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public String getCep() {
		return cep;
	}
	public void setCep(String cep) {
		this.cep = cep;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public String getNumero() {
		return numero;
	}
	public void setNumero(String numero) {
		this.numero = numero;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getHorario() {
		return horario;
	}
	public void setHorario(String horario) {
		this.horario = horario;
	}
	public String getDiaTrabalho() {
		return diaTrabalho;
	}
	public void setDiaTrabalho(String diaTrabalho) {
		this.diaTrabalho = diaTrabalho;
	}
	public String getMaterialReciclado() {
		return materialReciclado;
	}
	public void setMaterialReciclado(String materialReciclado) {
		this.materialReciclado = materialReciclado;
	}

	
}
